import { NavLink, Outlet, useNavigate } from 'react-router-dom';
import { useState } from 'react';

const navItems = [
  { to: '/hr/resume', label: 'Загрузка', icon: UploadIcon, end: true },
  { to: '/hr/resume/candidates', label: 'Кандидаты', icon: UsersIcon, end: false },
  { to: '/hr/resume/analytics', label: 'Аналитика', icon: ChartIcon, end: false },
  { to: '/hr/resume/archive', label: 'Архив', icon: ArchiveIcon, end: false },
  { to: '/hr/resume/trash', label: 'Корзина', icon: TrashIcon, end: false },
];

export default function ResumeLayout() {
  const navigate = useNavigate();
  const [copied, setCopied] = useState(false);

  const copyApplyLink = async () => {
    const url = `${window.location.origin}/resume/apply`;
    await navigator.clipboard.writeText(url);
    setCopied(true);
    setTimeout(() => setCopied(false), 2000);
  };

  return (
    <div className="flex h-[calc(100vh-64px)] -mx-6 -mt-6">
      <aside className="flex w-60 flex-col border-r border-gray-200 bg-white shrink-0">
        <div className="flex h-12 items-center gap-2 border-b border-gray-200 px-4">
          <div className="flex h-7 w-7 items-center justify-center rounded-lg bg-indigo-600 text-white text-xs font-bold">
            M
          </div>
          <span className="font-semibold text-gray-900 text-sm">MedHR</span>
        </div>

        <nav className="flex-1 space-y-0.5 p-2">
          {navItems.map((item) => (
            <NavLink
              key={item.to}
              to={item.to}
              end={item.end}
              className={({ isActive }) =>
                `flex items-center gap-3 rounded-lg px-3 py-2 text-sm transition-colors ${
                  isActive
                    ? 'bg-indigo-50 text-indigo-700 font-medium'
                    : 'text-gray-600 hover:bg-gray-100 hover:text-gray-900'
                }`
              }
            >
              <item.icon className="h-4 w-4" />
              {item.label}
            </NavLink>
          ))}
        </nav>

        <div className="border-t border-gray-200 p-3 space-y-2">
          <button
            onClick={copyApplyLink}
            className="flex w-full items-center gap-2 rounded-lg border border-gray-200 px-3 py-2 text-xs text-gray-600 hover:bg-gray-50 transition-colors"
          >
            <ExternalLinkIcon className="h-3.5 w-3.5" />
            <span>Ссылка для кандидата</span>
            {copied ? (
              <CheckIcon className="ml-auto h-3.5 w-3.5 text-green-500" />
            ) : (
              <CopyIcon className="ml-auto h-3.5 w-3.5" />
            )}
          </button>
          <button
            onClick={() => navigate('/hr')}
            className="flex w-full items-center gap-2 rounded-lg px-3 py-1.5 text-xs text-gray-500 hover:text-gray-700 transition-colors"
          >
            ← Назад к HR
          </button>
        </div>
      </aside>

      <main className="flex-1 overflow-y-auto p-6 bg-gray-50">
        <Outlet />
      </main>
    </div>
  );
}

